/**
 * Gets the current active language ('en' or 'pa')
 */
export function getCurrentLang() {
  const saved = localStorage.getItem('komal_creations_lang');
  return (saved === 'en' || saved === 'pa') ? saved : 'pa';
}

/**
 * Persists selected language code to local storage
 * @param {string} lang 
 */
export function setLanguage(lang) {
  if (lang === 'en' || lang === 'pa') {
    localStorage.setItem('komal_creations_lang', lang);
    updateLanguageDOM();
  }
}

/**
 * Scans the entire HTML Document and translates DOM elements
 * loaded with 'data-en' and 'data-pa' attributes.
 * Also handles elements with custom input placeholders like 'data-placeholder-en'.
 */
export function updateLanguageDOM() {
  const currentLang = getCurrentLang();
  
  // Set global language tag on body
  document.documentElement.lang = currentLang;

  // Update elements with translations
  const elements = document.querySelectorAll('[data-en][data-pa]');
  elements.forEach(el => {
    let val = el.getAttribute(`data-${currentLang}`) || '';
    // Unescape HTML entities so browser can parse them as DOM nodes in innerHTML
    val = val.replace(/&lt;/g, '<')
             .replace(/&gt;/g, '>')
             .replace(/&amp;/g, '&');

    if (val.includes('<') && val.includes('>')) {
      el.innerHTML = val;
    } else {
      el.textContent = val;
    }
  });

  // Update placeholders
  const inputs = document.querySelectorAll('[data-placeholder-en][data-placeholder-pa]');
  inputs.forEach(input => {
    const val = input.getAttribute(`data-placeholder-${currentLang}`) || '';
    input.setAttribute('placeholder', val);
  });
  
  // Dispatch custom event for dynamic javascript pages to reload their local layout
  const event = new CustomEvent('languageChanged', { detail: { lang: currentLang } });
  document.dispatchEvent(event);
  
  // Update toggle button text if exists
  const btn = document.getElementById('lang-switch-btn');
  if (btn) {
    btn.innerHTML = `🌐 ${currentLang === 'en' ? 'ਪੰਜਾਬੀ' : 'English'}`;
  }
}

// Bind load listener
document.addEventListener('DOMContentLoaded', () => {
  updateLanguageDOM();
  
  // Setup click triggers on language button
  const btn = document.getElementById('lang-switch-btn');
  if (btn) {
    btn.addEventListener('click', () => {
      const active = getCurrentLang();
      setLanguage(active === 'en' ? 'pa' : 'en');
    });
  }
});
